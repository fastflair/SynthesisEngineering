def _insert_sorted_by_page(script: List[Dict], new_page: Dict) -> None:
    """Insert new_page into script at the correct position (sorted by page num)."""
    page_num = int(new_page.get("page", 0))
    for i, pg in enumerate(script):
        if int(pg.get("page", 0)) > page_num:
            script.insert(i, new_page)
            return
    script.append(new_page)


def _insert_sorted_by_page_num(layouts_raw: List[Dict], new_layout: Dict) -> None:
    """Insert new_layout into layouts_raw sorted by page_number."""
    page_num = int(new_layout.get("page_number", 0))
    for i, l in enumerate(layouts_raw):
        if int(l.get("page_number", 0)) > page_num:
            layouts_raw.insert(i, new_layout)
            return
    layouts_raw.append(new_layout)


