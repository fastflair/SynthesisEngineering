"""
=============================================================================
COMIC BOOK VALIDATOR — Post-generation gap detector and repairman
=============================================================================
Validates a synthesized comic book by detecting and repairing:

  1. SKIPPED PAGES — Gaps in page numbering (e.g. pages 1,2,3,5 → page 4
     missing). Gap pages are generated by the LLM with full narrative context
     from surrounding pages so the story flows seamlessly through the repair.

  2. MISSING PANELS — Panels that exist in the layout but have no generated
     image (broken path, failed generation, or skipped entirely). Missing
     panel images are regenerated using the panel's existing script data or,
     if the script data is also missing, synthesized from the page beat.

After all repairs are applied the validator re-sorts pages, recomposes every
page image, and rebuilds the DOCX / PDF / CBZ outputs.

USAGE:
    from comic_book_validator import validate_and_repair_comic

    # After running synthesize_comic_book(...) or synthesize_and_validate(...)
    result = validate_and_repair_comic(
        working_dir="./SynthesizedComics/_work_MyComic",
        output_dir="./SynthesizedComics",
    )

    # Or as a one-shot wrapper that synthesizes + validates in sequence:
    from comic_book_validator import synthesize_and_validate

    result = synthesize_and_validate(
        story_idea_str=concept,
        target_pages=50,
        panels_per_page_avg=4,
        use_grok=True,
        image_model='ZIMAGE',
    )

=============================================================================
"""

