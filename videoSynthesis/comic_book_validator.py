def _load_artifacts(working_dir: str):
    """Load all working-directory artifacts.

    Returns
    -------
    (script, layouts_raw, manifest, project_data)
    """
    paths = {
        "script":   os.path.join(working_dir, "script.json"),
        "layouts":  os.path.join(working_dir, "layouts.json"),
        "manifest": os.path.join(working_dir, "panel_manifest.json"),
        "project":  os.path.join(working_dir, "project.pkl"),
    }
    for name, path in paths.items():
        if not os.path.exists(path):
            raise FileNotFoundError(f"Required artifact '{name}' not found: {path}")

    with open(paths["script"], encoding="utf-8") as f:
        script = json.load(f)
    with open(paths["layouts"], encoding="utf-8") as f:
        layouts_raw = json.load(f)
    with open(paths["manifest"], encoding="utf-8") as f:
        manifest = json.load(f)
    with open(paths["project"], "rb") as f:
        project_data = pickle.load(f)

    return script, layouts_raw, manifest, project_data


def _save_artifacts(working_dir: str,
                    script: List[Dict],
                    layouts_raw: List[Dict],
                    manifest: Dict) -> None:
    """Persist updated artifacts back to working_dir."""
    with open(os.path.join(working_dir, "script.json"), "w", encoding="utf-8") as f:
        json.dump(script, f, indent=2, ensure_ascii=False)
    with open(os.path.join(working_dir, "layouts.json"), "w", encoding="utf-8") as f:
        json.dump(layouts_raw, f, indent=2, ensure_ascii=False)
    with open(os.path.join(working_dir, "panel_manifest.json"), "w", encoding="utf-8") as f:
        json.dump(manifest, f, indent=2, ensure_ascii=False)


def _layouts_raw_to_objects(layouts_raw: List[Dict]) -> list:
    """Convert raw layout dicts → PageLayout dataclass instances."""
    m = _cbg()
    result = []
    for l in layouts_raw:
        result.append(m.PageLayout(
            page_number=l["page_number"],
            template_name=l["template_name"],
            panel_count=l["panel_count"],
            panel_boxes=[tuple(b) for b in l["panel_boxes"]],
            page_title=l.get("page_title", ""),
            page_mood=l.get("page_mood", ""),
            cliffhanger=l.get("cliffhanger", False),
        ))
    return result


def _layout_to_raw(layout) -> Dict:
    """Convert a PageLayout dataclass → JSON-serialisable dict."""
    return {
        "page_number":   layout.page_number,
        "template_name": layout.template_name,
        "panel_count":   layout.panel_count,
        "panel_boxes":   [list(b) for b in layout.panel_boxes],
        "page_title":    layout.page_title,
        "page_mood":     layout.page_mood,
        "cliffhanger":   layout.cliffhanger,
    }


