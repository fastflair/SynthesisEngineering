def _regenerate_panel_images(page_num: int,
                              missing_panel_indices: List[int],
                              page_script: Dict,
                              layout_raw: Dict,
                              manifest: Dict,
                              project,
                              pipe,
                              panels_dir: str,
                              panel_variants: int) -> Dict:
    """Generate images for specific panel indices that are absent or broken.

    Uses _nearest_gen_size with the true panel slot dimensions (derived from
    layout boxes + page geometry) so the generated image AR matches the
    panel slot exactly — the same approach as the main pipeline. Falls back
    to a symmetric nearest-size if layout info is unavailable.
    """
    m = _cbg()

    page_key = f"page_{page_num:03d}"
    if page_key not in manifest:
        manifest[page_key] = {}
    page_manifest = manifest[page_key]

    # ── Emotional arc state and DNA ──────────────────────────────────────────
    emotional_arc = (project.story_dna or {}).get('_emotional_arc', {})
    arc_state     = emotional_arc.get(page_num, {})
    page_dna      = m.build_page_dna(page_script, project.story_dna, page_num, arc_state)

    # ── Active image model ───────────────────────────────────────────────────
    import novel_generator as _ng
    _active_model = getattr(_ng, 'IMAGE_MODEL', None)

    # ── Panel script lookup ──────────────────────────────────────────────────
    panel_scripts: Dict[int, Dict] = {}
    for ps in (page_script.get("panels") or []):
        if isinstance(ps, dict):
            idx = ps.get("panel_index")
            if idx:
                panel_scripts[int(idx)] = ps

    panel_boxes = layout_raw.get("panel_boxes", [])
    all_panel_indices = sorted(panel_scripts.keys()) or list(range(1, len(panel_boxes) + 1))

    # ── Compute true pixel slot dimensions once for all panels on this page ──
    # Build a minimal PageLayout so _compute_page_panel_pixel_dims can derive
    # the exact panel slot sizes (margins + gutters applied), matching how the
    # main pipeline sizes images.
    try:
        _tmp_layout = m.PageLayout(
            page_number   = page_num,
            template_name = layout_raw.get("template_name", ""),
            panel_count   = layout_raw.get("panel_count", len(panel_boxes)),
            panel_boxes   = [tuple(b) for b in panel_boxes],
            page_title    = layout_raw.get("page_title", ""),
            page_mood     = layout_raw.get("page_mood", ""),
            cliffhanger   = layout_raw.get("cliffhanger", False),
        )
        _pixel_dims = m._compute_page_panel_pixel_dims(_tmp_layout)
    except Exception:
        _pixel_dims = []

    # ── Continuity anchors ───────────────────────────────────────────────────
    def _panel_summary(pidx: int) -> str:
        ps = panel_scripts.get(pidx, {})
        desc = (ps.get('description', '') or '').strip()[:130]
        comp = ps.get('composition', '')
        return f"{desc} [{comp}]" if comp else desc

    min_panel = min(all_panel_indices, default=1)
    first_panel_is_missing = (min_panel in missing_panel_indices)

    page_anchor_ctx: Optional[Dict] = None
    if not first_panel_is_missing:
        for pidx in sorted(all_panel_indices):
            if pidx not in missing_panel_indices:
                page_anchor_ctx = {
                    'summary':        _panel_summary(pidx),
                    'palette_state':  arc_state.get('palette_state', ''),
                    'lighting_state': arc_state.get('lighting_state', ''),
                }
                break

    # Reorder: generate panel 1 first (it defines scene space)
    ordered_missing = list(missing_panel_indices)
    if first_panel_is_missing and min_panel in ordered_missing:
        ordered_missing.remove(min_panel)
        ordered_missing.insert(0, min_panel)

    for panel_idx in ordered_missing:
        panel_key = f"panel_{panel_idx:02d}"
        logger.info(f"    Generating images: page {page_num} panel {panel_idx}…")

        # ── Build panel script ───────────────────────────────────────────────
        ps = panel_scripts.get(panel_idx)
        if not ps:
            n_panels = len(all_panel_indices) or 4
            if panel_idx == min(all_panel_indices, default=1):
                comp = 'establishing'
            elif panel_idx == max(all_panel_indices, default=n_panels):
                comp = 'close_up'
            else:
                comp = 'medium_shot'
            ps = {
                "panel_index":         panel_idx,
                "description":         (page_script.get("beat")
                                         or page_script.get("title")
                                         or f"Story moment on page {page_num}"),
                "composition":         comp,
                "camera_angle":        "eye_level",
                "mood":                page_script.get("emotional_tone", ""),
                "characters_in_frame": page_script.get("characters_present", []),
                "sensory_anchor":      page_script.get("visual_hook", ""),
                "importance":          "standard",
                "dialogue":            [],
                "sfx":                 [],
            }

        # ── Panel DNA ────────────────────────────────────────────────────────
        panel_dna = m.build_panel_dna(ps, page_dna, project.story_dna, arc_state)

        # ── Previous-panel continuity ────────────────────────────────────────
        prev_panel_ctx: Optional[Dict] = None
        prev_idx = panel_idx - 1
        if prev_idx >= 1:
            prev_summary = _panel_summary(prev_idx)
            if prev_summary:
                prev_panel_ctx = {
                    'summary': prev_summary,
                    'transition_style': arc_state.get('transition_style', ''),
                }

        # ── Scene consistency anchor ─────────────────────────────────────────
        _scene_anchor = None
        try:
            import comic_book_art_director as _ad
            _scene_anchor = _ad.build_scene_consistency_anchor(
                ps, project.characters,
                registry=project.character_registry,
                visual_bible=getattr(project, 'visual_bible', None),
            )
        except Exception as _e:
            logger.warning(f"      [SceneAnchor] regen build failed: {_e}")

        # ── Image prompt ─────────────────────────────────────────────────────
        prompt = m.compose_panel_prompt(
            ps, project.story_idea, project.character_registry,
            project.story_dna, page_dna, panel_dna,
            prev_panel_ctx=prev_panel_ctx,
            page_anchor_ctx=page_anchor_ctx,
            visual_bible=getattr(project, 'visual_bible', None),
            scene_consistency_ctx=_scene_anchor,
        )

        # ── Image dimensions: use true panel slot AR via _nearest_gen_size ───
        # Mirror the main pipeline exactly: get the pixel slot dims from the
        # layout (margins + gutters), pass to _nearest_gen_size for the model-
        # legal (gen_w, gen_h) that best matches the slot's aspect ratio.
        box_idx = panel_idx - 1
        if 0 <= box_idx < len(_pixel_dims):
            true_w, true_h = _pixel_dims[box_idx]
        elif 0 <= box_idx < len(panel_boxes):
            # Fallback: rough estimate from box fractions × printable area
            x, y, w_pct, h_pct = panel_boxes[box_idx]
            _pw = int((8.5 - 1.0) * 300)   # printable width at 300 DPI
            _ph = int((11.0 - 1.125) * 300) # printable height at 300 DPI
            true_w = max(64, int(w_pct * _pw))
            true_h = max(64, int(h_pct * _ph))
        else:
            true_w, true_h = 1024, 1024

        gen_w, gen_h = m._nearest_gen_size(true_w, true_h, _active_model)

        # ── Generate variants ────────────────────────────────────────────────
        variant_records = []
        arc_intensity = float(arc_state.get('intensity', 0.5) or 0.5)
        grammar_label = panel_dna.get('grammar_label', '')

        try:
            _chars_seed  = ps.get('characters_in_frame', []) or []
            _scene_setting = (ps.get('setting') or '').strip()
            _base_seed   = m._derive_base_seed(
                f"{m._scene_seed_key(_scene_setting, _chars_seed)}|panel{panel_idx}"
            )
        except Exception:
            _base_seed = random.randint(0, m.MAX_SEED)

        for v_idx in range(panel_variants):
            seed       = (_base_seed + v_idx * 0x9E3779B1) % (m.MAX_SEED + 1)
            variant_no = v_idx + 1
            fname      = f"{page_key}_{panel_key}_variant_{variant_no:02d}.jpg"
            save_path  = os.path.join(panels_dir, fname)
            try:
                img = m._safe_gen_image(
                    pipe, prompt, height=gen_h, width=gen_w,
                    seed=seed, num_inference_steps=m.N_STEPS,
                    promptEnhance=False,
                )
                img_np   = np.array(img)
                enhanced = m.comic_panel_pipeline(
                    img_np,
                    grammar_label=grammar_label,
                    arc_intensity=arc_intensity,
                )
                try:
                    _vscore, _ = m._score_panel_variant(enhanced)
                except Exception:
                    _vscore = -1e9
                Image.fromarray(enhanced).save(
                    save_path, format="JPEG",
                    quality=m.PRINT_PANEL_JPEG_QUALITY, optimize=True,
                )
                variant_records.append({
                    "index": variant_no, "path": save_path,
                    "seed": seed, "score": _vscore,
                })
            except Exception as exc:
                logger.warning(f"      Variant {variant_no} failed: {exc}")

        if variant_records:
            try:
                _best = m._select_best_variant(variant_records)
            except Exception:
                _best = 1
            page_manifest[panel_key] = {
                "active_variant":  _best,
                "prompt":          prompt,
                "description":     ps.get("description", ""),
                "variants":        variant_records,
                "true_panel_w":    true_w,
                "true_panel_h":    true_h,
                "gen_dims":        [gen_w, gen_h],
                "visual_grammar":  grammar_label,
                "arc_intensity":   arc_intensity,
                "arc_stage":       arc_state.get('stage', ''),
            }
            logger.info(f"      ✓ {len(variant_records)} variant(s) generated "
                        f"(selected variant {_best})")

            # After generating panel 1, use it as the page anchor
            if page_anchor_ctx is None or panel_idx == min_panel:
                page_anchor_ctx = {
                    'summary':        ps.get('description', '')[:130],
                    'palette_state':  arc_state.get('palette_state', ''),
                    'lighting_state': arc_state.get('lighting_state', ''),
                }
        else:
            logger.warning(f"      ✗ All variants failed for page {page_num} panel {panel_idx}")

    return manifest


