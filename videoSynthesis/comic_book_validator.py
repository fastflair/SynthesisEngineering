# SECTION 7 — CONVENIENCE WRAPPER: synthesize + validate in one call
# =============================================================================

def synthesize_and_validate(
    story_idea_str:      str,
    target_pages              = 24,
    panels_per_page_avg: int  = 4,
    creative_seeds:      Dict = None,
    cast_size:           str  = "medium",
    save_dir:            str  = "./SynthesizedComics",
    cover_variants:      int  = None,
    panel_variants:      int  = None,
    pipe                      = None,
    load_previous:       bool = False,
    previous_pickle:     str  = None,
    use_grok:            bool = None,
    image_model:         str  = None,
    scene_diversity           = 'auto',
) -> Dict[str, Any]:
    """Run synthesize_comic_book() then immediately validate_and_repair_comic().

    Parameters mirror synthesize_comic_book() exactly. Returns a merged dict
    with both synthesis and validation results (docx, pdf, cbz, working_dir,
    manifest, issues_found, missing_pages, pages_with_missing_panels).

    The final outputs are always named {title}_comic.docx / .pdf / .cbz —
    validation is a quality-assurance pass, not a separate deliverable. There
    are never *_validated.* files produced by this function.

    Usage
    -----
    from comic_book_validator import synthesize_and_validate

    result = synthesize_and_validate(
        story_idea_str=STORY_IDEA,
        cast_size='auto',
        target_pages='auto',
        panels_per_page_avg=3,
        use_grok=True,
        image_model='ZIMAGE',
    )
    print(result["pdf"])
    """
    m = _cbg()

    synth_kwargs: Dict[str, Any] = dict(
        story_idea_str      = story_idea_str,
        target_pages        = target_pages,
        panels_per_page_avg = panels_per_page_avg,
        creative_seeds      = creative_seeds,
        cast_size           = cast_size,
        save_dir            = save_dir,
        load_previous       = load_previous,
        previous_pickle     = previous_pickle,
    )
    if cover_variants is not None:
        synth_kwargs['cover_variants'] = cover_variants
    if panel_variants is not None:
        synth_kwargs['panel_variants'] = panel_variants
    if pipe is not None:
        synth_kwargs['pipe'] = pipe
    if use_grok is not None:
        synth_kwargs['use_grok'] = use_grok
    if image_model is not None:
        synth_kwargs['image_model'] = image_model
    if scene_diversity != 'auto':
        synth_kwargs['scene_diversity'] = scene_diversity

    logger.info("Phase A: Synthesizing comic book…")
    synth_result = m.synthesize_comic_book(**synth_kwargs)

    working_dir = synth_result["working_dir"]

    logger.info("\nPhase B: Validating and repairing…")
    repair_result = validate_and_repair_comic(
        working_dir         = working_dir,
        output_dir          = save_dir,
        panels_per_page_avg = panels_per_page_avg,
        pipe                = pipe,
        panel_variants      = panel_variants,   # pass through to repair phase
        use_grok            = use_grok,
        image_model         = image_model,
    )

    # Validation paths overwrite synthesis paths in the merged result so callers
    # always reference the latest (repaired) files.
    return {**synth_result, **repair_result}


