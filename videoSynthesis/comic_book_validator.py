def _page_context_snippet(page: Dict, role: str) -> str:
    """One-line context string from a page dict for LLM prompting."""
    title   = page.get("title", "")
    beat    = page.get("beat", "")
    setting = page.get("setting", "")
    tone    = page.get("emotional_tone", "")
    panels  = page.get("panels") or []

    edge_desc = ""
    if panels:
        target_panel = panels[-1] if role == "preceding" else panels[0]
        if isinstance(target_panel, dict):
            edge_desc = (target_panel.get("description") or "")[:100]

    label = "ends with" if role == "preceding" else "opens with"
    return (
        f"Title: {title!r} | Beat: {beat} | "
        f"Setting: {setting} | Tone: {tone} | {label}: {edge_desc}"
    )


def _normalise_panels(raw_panels: list, fallback_context: str, page_num: int) -> List[Dict]:
    """Coerce whatever the LLM returned into clean panel dicts."""
    clean = []
    for idx, panel in enumerate(raw_panels, 1):
        if isinstance(panel, dict):
            panel["panel_index"] = idx
            if not (panel.get("description") or "").strip():
                panel["description"] = fallback_context or f"Story moment on page {page_num}"
            clean.append(panel)
        else:
            clean.append({
                "panel_index":        idx,
                "description":        str(panel) if panel else fallback_context,
                "composition":        "medium_shot",
                "camera_angle":       "eye_level",
                "mood":               "",
                "characters_in_frame": [],
                "sensory_anchor":     "",
                "importance":         "standard",
                "dialogue":           [],
                "sfx":                [],
            })
    return clean


def generate_gap_page_scripts(missing_pages: List[int],
                               script: List[Dict],
                               project,
                               panels_per_page_avg: int = 4) -> List[Dict]:
    """Generate complete panel scripts for each missing page.

    Each gap page is created by asking the LLM to bridge the narrative
    gap using the surrounding pages as context.
    """
    if not missing_pages:
        return []

    m = _cbg()

    script_by_page: Dict[int, Dict] = {}
    for pg in script:
        if pg.get("_act_break") or pg.get("_prologue"):
            continue
        pnum = pg.get("page")
        if pnum is not None:
            script_by_page[int(pnum)] = pg

    all_nums = sorted(script_by_page)
    si = project.story_idea
    generated: List[Dict] = []

    for page_num in missing_pages:
        logger.info(f"  Generating script for missing page {page_num}…")

        preceding_nums = [n for n in all_nums if n < page_num]
        following_nums = [n for n in all_nums if n > page_num]
        pre_page  = script_by_page.get(preceding_nums[-1]) if preceding_nums else None
        post_page = script_by_page.get(following_nums[0])  if following_nums else None

        pre_summary  = (_page_context_snippet(pre_page,  "preceding")
                        if pre_page  else "Beginning of story")
        post_summary = (_page_context_snippet(post_page, "following")
                        if post_page else "End of story")

        context_lines = []
        for n in (preceding_nums[-4:] + following_nums[:4]):
            pg = script_by_page.get(n)
            if pg:
                context_lines.append(
                    f"  Page {n}: {pg.get('beat', '')} [{pg.get('emotional_tone', '')}]"
                )
        wider_context = "\n".join(context_lines) or "  (no wider context)"

        pre_page_num  = preceding_nums[-1] if preceding_nums else "?"
        post_page_num = following_nums[0]  if following_nums else "?"
        min_panels    = max(1, panels_per_page_avg - 1)
        max_panels    = panels_per_page_avg + 1

        prompt = (
            "You are a comic book writer. A page is MISSING from the story "
            "and must be written to maintain narrative continuity.\n\n"
            f"STORY CONTEXT:\n"
            f"  Genre:   {si.genre}\n"
            f"  Mood:    {si.mood}\n"
            f"  Premise: {si.premise[:300]}\n\n"
            f"MISSING PAGE: {page_num}\n\n"
            f"PRECEDING PAGE ({pre_page_num}):\n  {pre_summary}\n\n"
            f"FOLLOWING PAGE ({post_page_num}):\n  {post_summary}\n\n"
            f"SURROUNDING PAGES (for broader continuity):\n{wider_context}\n\n"
            f"Write page {page_num} so it bridges the preceding page's final "
            "moment to the following page's opening beat. Requirements:\n"
            f"  • Follow naturally from the preceding page\n"
            f"  • Lead naturally into the following page\n"
            f"  • Maintain genre, mood and premise\n"
            f"  • Include {min_panels}–{max_panels} panels\n\n"
            "Return a SINGLE JSON object (no markdown, no explanation):\n"
            "{\n"
            f'  "page": {page_num},\n'
            '  "title": "evocative 3-6 word title",\n'
            '  "setting": "vivid setting description",\n'
            '  "characters_present": ["name1"],\n'
            '  "beat": "single sentence — the story beat this page delivers",\n'
            '  "beat_id": "",\n'
            '  "emotional_tone": "one evocative phrase",\n'
            '  "visual_hook": "the panel a reader scans first",\n'
            '  "cliffhanger": false,\n'
            '  "importance": "standard",\n'
            '  "panels": [\n'
            '    {\n'
            '      "panel_index": 1,\n'
            '      "description": "WHAT IS IN FRAME — visual, specific, concrete",\n'
            '      "composition": "medium_shot",\n'
            '      "camera_angle": "eye_level",\n'
            '      "mood": "evocative phrase",\n'
            '      "characters_in_frame": ["name1"],\n'
            '      "sensory_anchor": "one specific sensory detail",\n'
            '      "importance": "standard",\n'
            '      "dialogue": [{"speaker":"NAME","text":"Short line.","bubble_type":"speech","position_hint":"top-left"}],\n'
            '      "sfx": []\n'
            '    }\n'
            '  ]\n'
            "}"
        )

        try:
            response = m.get_openai_prompt_response(
                prompt, temperature=0.7, max_completion_tokens=75000,
                openai_model=m.openai_model_large, use_grok=m.USE_GROK,
            )
            parsed = m.parse_json_response(response)

            if parsed and isinstance(parsed, dict) and parsed.get("panels"):
                parsed["page"] = page_num
                fallback_beat  = parsed.get("beat", f"Story beat on page {page_num}")
                parsed["panels"] = _normalise_panels(
                    parsed["panels"], fallback_beat, page_num
                )
                generated.append(parsed)
                logger.info(
                    f"    ✓ Page {page_num}: '{parsed.get('title','')}' "
                    f"({len(parsed['panels'])} panels)"
                )
                continue

        except Exception as exc:
            logger.warning(f"    LLM failed for gap page {page_num}: {exc}")

        # ── Fallback: minimal page derived from surrounding beats ────────────
        pre_beat  = (pre_page  or {}).get("beat", "")
        post_beat = (post_page or {}).get("beat", "")
        ref_page  = pre_page or post_page or {}

        fallback: Dict[str, Any] = {
            "page":               page_num,
            "title":              f"Page {page_num}",
            "setting":            ref_page.get("setting", ""),
            "characters_present": ref_page.get("characters_present", []),
            "beat":               f"Transition from [{pre_beat[:80]}] to [{post_beat[:80]}]",
            "beat_id":            "",
            "emotional_tone":     ref_page.get("emotional_tone", ""),
            "visual_hook":        "dramatic transition",
            "cliffhanger":        False,
            "importance":         "standard",
            "panels": [
                {
                    "panel_index":        i + 1,
                    "description":        (
                        pre_beat[:120]  if i == 0
                        else post_beat[:120] if i == panels_per_page_avg - 1
                        else f"Transition moment {i+1} bridging the narrative gap"
                    ),
                    "composition":        "medium_shot",
                    "camera_angle":       "eye_level",
                    "mood":               ref_page.get("emotional_tone", "dramatic"),
                    "characters_in_frame": ref_page.get("characters_present", [])[:2],
                    "sensory_anchor":     "",
                    "importance":         "standard",
                    "dialogue":           [],
                    "sfx":                [],
                }
                for i in range(panels_per_page_avg)
            ],
        }
        generated.append(fallback)
        logger.warning(f"    ⚠ Used fallback script for page {page_num}")

    return generated


