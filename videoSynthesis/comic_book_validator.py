# SECTION 6 — MAIN VALIDATOR / REPAIRMAN
# =============================================================================

def validate_and_repair_comic(
    working_dir:         str,
    output_dir:          str,
    panels_per_page_avg: int           = 4,
    pipe                               = None,
    panel_variants:      Optional[int] = None,
    use_grok:            Optional[bool] = None,
    image_model:         Optional[str]  = None,
) -> Dict[str, Any]:
    """Validate and repair a synthesized comic book.

    Detects skipped pages and missing panel images, repairs all issues, then
    rebuilds the DOCX / PDF / CBZ with the SAME filenames as the primary
    synthesis outputs ({title}_comic.docx / .pdf / .cbz) — no _validated
    suffix. Repaired files overwrite the originals so there is always exactly
    one complete set of outputs.

    Parameters
    ----------
    working_dir         Path to the comic's working directory (contains
                        script.json, layouts.json, panel_manifest.json,
                        project.pkl).
    output_dir          Directory for rebuilt output files.
    panels_per_page_avg Average panels per page when generating gap pages.
    pipe                Optional pre-loaded image pipeline.
    panel_variants      Variants per missing panel (default: PANEL_VARIANTS).
    use_grok            Override USE_GROK for this run.
    image_model         Override IMAGE_MODEL for this run.

    Returns
    -------
    {
        'issues_found':              int,
        'missing_pages':             List[int],
        'pages_with_missing_panels': Dict[int, List[int]],
        'docx':  str, 'pdf': str, 'cbz': str,
        'working_dir': str,
    }
    """
    m = _cbg()

    import novel_generator as _ng
    _orig_use_grok    = _ng.USE_GROK
    _orig_image_model = _ng.IMAGE_MODEL
    if use_grok is not None:
        _ng.USE_GROK = bool(use_grok)
        if hasattr(m, 'USE_GROK'):
            m.USE_GROK = _ng.USE_GROK
    if image_model is not None:
        if image_model not in ('ZIMAGE', 'ERNIE', 'CHROMA', 'LENS', 'KLEIN2'):
            raise ValueError(
                f"image_model must be 'ZIMAGE', 'ERNIE', 'CHROMA', 'LENS', or 'KLEIN2',"
                f" got {image_model!r}"
            )
        _ng.IMAGE_MODEL = image_model
    if panel_variants is None:
        panel_variants = m.PANEL_VARIANTS

    logger.info("=" * 62)
    logger.info("COMIC BOOK VALIDATOR — Detecting gaps…")
    logger.info("=" * 62)

    # ── 1. Load ──────────────────────────────────────────────────────────────
    script, layouts_raw, manifest, project_data = _load_artifacts(working_dir)
    project     = project_data["project"]
    cover_paths = project_data.get("cover_paths", [])
    logger.info(f"Loaded: {len(script)} script pages, {len(layouts_raw)} layouts")

    # ── 2. Detect ─────────────────────────────────────────────────────────────
    gap_report = detect_gaps(script, layouts_raw, manifest)
    logger.info("\n" + gap_report["summary"])

    missing_pages             = gap_report["missing_pages"]
    pages_with_missing_panels = gap_report["pages_with_missing_panels"]

    # ── Output paths — always _comic.* (never _validated) ────────────────────
    safe = m.cleanup_special_chars(project.title)
    m.create_directory_if_not_exists(output_dir)
    docx_path = os.path.join(output_dir, f"{safe}_comic.docx")
    pdf_path  = os.path.join(output_dir, f"{safe}_comic.pdf")
    cbz_path  = os.path.join(output_dir, f"{safe}_comic.cbz")
    pkl_path  = os.path.join(working_dir, "project.pkl")

    # ── 3. Short-circuit if nothing to repair ─────────────────────────────────
    if not gap_report["total_issues"]:
        logger.info("\nNo repairs needed. Rebuilding outputs from current state…")
        layouts    = _layouts_raw_to_objects(layouts_raw)
        page_paths = m.compose_all_pages(project, script, layouts, manifest)
        m.assemble_docx(project, page_paths, cover_paths, docx_path,
                        manifest=manifest, page_count=len(page_paths),
                        script=script, layouts=layouts)
        m.assemble_pdf(page_paths, cover_paths, pdf_path, project,
                       manifest=manifest)
        m.assemble_cbz(page_paths, cover_paths, cbz_path,
                       project_pkl=pkl_path if os.path.exists(pkl_path) else None,
                       manifest=manifest)
        _ng.USE_GROK    = _orig_use_grok
        _ng.IMAGE_MODEL = _orig_image_model
        if hasattr(m, 'USE_GROK'):
            m.USE_GROK = _orig_use_grok
        return {
            "issues_found":              0,
            "missing_pages":             [],
            "pages_with_missing_panels": {},
            "docx": docx_path, "pdf": pdf_path, "cbz": cbz_path,
            "working_dir": working_dir,
        }

    # ── 4. Load image pipeline ────────────────────────────────────────────────
    own_pipe = pipe is None
    if own_pipe:
        logger.info("\nLoading image generation pipeline…")
        pipe = m.load_ImageZ_pipe()

    panels_dir = os.path.join(working_dir, "panels")
    m.create_directory_if_not_exists(panels_dir)

    try:
        # ── 5. Repair: missing pages ──────────────────────────────────────────
        if missing_pages:
            logger.info(f"\n{'─'*50}")
            logger.info(f"Repairing {len(missing_pages)} missing page(s): {missing_pages}")
            logger.info("─" * 50)

            new_page_scripts = generate_gap_page_scripts(
                missing_pages, script, project, panels_per_page_avg
            )

            for new_page in new_page_scripts:
                page_num    = int(new_page["page"])
                raw_panels  = new_page.get("panels") or []
                panel_count = max(1, len(raw_panels))

                # Use the current emotional arc state for layout selection
                emotional_arc = (project.story_dna or {}).get('_emotional_arc', {})
                arc_state = emotional_arc.get(page_num, {})
                arc_intensity = float(arc_state.get('intensity', 0.5) or 0.5)

                template = m._pick_layout_template(
                    panel_count,
                    new_page.get("importance", "standard"),
                    last_panel_importance=((raw_panels[-1].get("importance", "standard")
                                            if raw_panels else "standard")),
                    page_number=page_num,
                    arc_intensity=arc_intensity,
                    is_cliffhanger=bool(new_page.get("cliffhanger", False)),
                )
                boxes = list(m.LAYOUT_TEMPLATES.get(template,
                                                     m.LAYOUT_TEMPLATES["two_by_two_4"]))
                boxes = boxes[:panel_count]
                # Apply the gap-fill pass so panels always tile the full page
                boxes = m._fill_layout_gaps(boxes)

                new_layout = m.PageLayout(
                    page_number   = page_num,
                    template_name = template,
                    panel_count   = len(boxes),
                    panel_boxes   = boxes,
                    page_title    = new_page.get("title", ""),
                    page_mood     = new_page.get("emotional_tone", ""),
                    cliffhanger   = bool(new_page.get("cliffhanger", False)),
                )
                new_layout_raw = _layout_to_raw(new_layout)

                manifest = _regenerate_panel_images(
                    page_num              = page_num,
                    missing_panel_indices = list(range(1, len(boxes) + 1)),
                    page_script           = new_page,
                    layout_raw            = new_layout_raw,
                    manifest              = manifest,
                    project               = project,
                    pipe                  = pipe,
                    panels_dir            = panels_dir,
                    panel_variants        = panel_variants,
                )

                _insert_sorted_by_page(script, new_page)
                _insert_sorted_by_page_num(layouts_raw, new_layout_raw)

                logger.info(f"  ✓ Page {page_num} inserted ({len(boxes)} panels)")

        # ── 6. Repair: missing panels within existing pages ───────────────────
        if pages_with_missing_panels:
            logger.info(f"\n{'─'*50}")
            logger.info(
                f"Repairing missing panels in "
                f"{len(pages_with_missing_panels)} page(s)…"
            )
            logger.info("─" * 50)

            script_by_page    = {int(pg.get("page", 0)): pg for pg in script}
            layout_raw_by_page = {int(l.get("page_number", 0)): l for l in layouts_raw}

            for page_num, bad_panels in sorted(pages_with_missing_panels.items()):
                logger.info(f"  Page {page_num}: panels {bad_panels}")
                page_script = script_by_page.get(page_num, {})
                layout_raw  = layout_raw_by_page.get(page_num, {})

                manifest = _regenerate_panel_images(
                    page_num              = page_num,
                    missing_panel_indices = bad_panels,
                    page_script           = page_script,
                    layout_raw            = layout_raw,
                    manifest              = manifest,
                    project               = project,
                    pipe                  = pipe,
                    panels_dir            = panels_dir,
                    panel_variants        = panel_variants,
                )

    finally:
        if own_pipe and pipe is not None:
            try:
                del pipe
                m.reset_memory(m.device)
            except Exception:
                pass

    # ── 7. Re-sort everything by page number ──────────────────────────────────
    script.sort(key=lambda pg: int(pg.get("page", 0)))
    layouts_raw.sort(key=lambda l: int(l.get("page_number", 0)))

    # ── 8. Persist updated artifacts ──────────────────────────────────────────
    _save_artifacts(working_dir, script, layouts_raw, manifest)
    logger.info("\nUpdated artifacts saved to working directory.")

    # ── 9. Recompose all pages ─────────────────────────────────────────────────
    layouts    = _layouts_raw_to_objects(layouts_raw)
    logger.info(f"\nRecomposing {len(layouts)} pages…")
    page_paths = m.compose_all_pages(project, script, layouts, manifest)

    # ── 10. Rebuild output files ───────────────────────────────────────────────
    m.assemble_docx(project, page_paths, cover_paths, docx_path,
                    manifest=manifest, page_count=len(page_paths),
                    script=script, layouts=layouts)
    m.assemble_pdf(page_paths, cover_paths, pdf_path, project,
                   manifest=manifest)
    m.assemble_cbz(page_paths, cover_paths, cbz_path,
                   project_pkl=pkl_path if os.path.exists(pkl_path) else None,
                   manifest=manifest)

    # ── 11. Final report ───────────────────────────────────────────────────────
    repaired_pages  = len(missing_pages)
    repaired_panels = sum(len(v) for v in pages_with_missing_panels.values())

    logger.info("\n" + "=" * 62)
    logger.info("VALIDATION & REPAIR COMPLETE")
    logger.info("=" * 62)
    logger.info(f"  Issues found:           {gap_report['total_issues']}")
    logger.info(f"  Missing pages repaired:  {repaired_pages}")
    logger.info(f"  Missing panels repaired: {repaired_panels}")
    logger.info(f"  Total pages in comic:    {len(script)}")
    logger.info(f"  DOCX: {docx_path}")
    logger.info(f"  PDF:  {pdf_path}")
    logger.info(f"  CBZ:  {cbz_path}")
    logger.info("=" * 62)

    _ng.USE_GROK    = _orig_use_grok
    _ng.IMAGE_MODEL = _orig_image_model
    if hasattr(m, 'USE_GROK'):
        m.USE_GROK = _orig_use_grok

    return {
        "issues_found":              gap_report["total_issues"],
        "missing_pages":             missing_pages,
        "pages_with_missing_panels": pages_with_missing_panels,
        "docx":        docx_path,
        "pdf":         pdf_path,
        "cbz":         cbz_path,
        "working_dir": working_dir,
    }


