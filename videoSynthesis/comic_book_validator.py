def detect_gaps(script: List[Dict],
                layouts_raw: List[Dict],
                manifest: Dict) -> Dict[str, Any]:
    """Scan the comic for missing pages and missing panel images.

    Special pages (_act_break, _prologue, _narration_splash, _act_interstitial)
    are excluded from gap detection: they are synthesized on demand and do not
    form part of the numbered story-page sequence.

    Returns
    -------
    {
        'script_page_numbers':       set[int],
        'expected_range':            range,
        'missing_pages':             List[int],
        'pages_with_missing_panels': Dict[int, List[int]],
        'total_issues':              int,
        'summary':                   str,
    }
    """
    def _is_special(page: Dict) -> bool:
        """True for non-story pages that skip normal numbering."""
        return bool(
            page.get("_act_break")
            or page.get("_prologue")
            or page.get("_narration_splash")
            or page.get("_act_interstitial")
            or page.get("importance") in ("act_interstitial", "narration_splash")
            or page.get("beat_type") in ("act_interstitial", "narration_splash")
        )

    # ── Index script by page number (story pages only) ──────────────────────
    script_by_page: Dict[int, Dict] = {}
    special_page_nums: set = set()   # page numbers occupied by act-break / special pages
    for page in script:
        pnum = page.get("page")
        if pnum is None:
            continue
        if _is_special(page) or page.get("_act_break"):
            # Act-break and other special pages occupy a page-number slot but
            # are not story pages. Record their numbers so detect_gaps does NOT
            # report those slots as missing — the act-break card IS the content.
            special_page_nums.add(int(pnum))
        else:
            script_by_page[int(pnum)] = page

    # ── Index layouts by page number ─────────────────────────────────────────
    layout_by_page: Dict[int, Dict] = {}
    for l in layouts_raw:
        pnum = l.get("page_number")
        if pnum is not None:
            layout_by_page[int(pnum)] = l

    all_page_nums = sorted(script_by_page)
    if not all_page_nums:
        return {
            "script_page_numbers":       set(),
            "expected_range":            range(0),
            "missing_pages":             [],
            "pages_with_missing_panels": {},
            "total_issues":              0,
            "summary":                   "No story pages found in script.json.",
        }

    min_page = 1
    max_page = max(all_page_nums)
    expected = set(range(min_page, max_page + 1))
    present  = set(all_page_nums)
    # A page-number slot is genuinely missing ONLY if it is neither a story
    # page NOR a special page (act-break, narration-splash, interstitial).
    # Without this exclusion, the act-break pages' slots (e.g. page 1, 23, 45)
    # are wrongly treated as gaps and the validator overwrites the act-break
    # title cards with newly-generated generic story pages.
    missing_pages = sorted((expected - present) - special_page_nums)

    # ── Detect panels with missing / broken image files ──────────────────────
    pages_with_missing_panels: Dict[int, List[int]] = {}

    for page_num, page in script_by_page.items():
        page_key = f"page_{page_num:03d}"
        page_manifest = manifest.get(page_key, {})

        layout = layout_by_page.get(page_num, {})
        panel_count = layout.get("panel_count", len(page.get("panels") or []))

        bad_panels: List[int] = []
        for panel_idx in range(1, panel_count + 1):
            panel_key = f"panel_{panel_idx:02d}"
            record = page_manifest.get(panel_key)

            if not record:
                bad_panels.append(panel_idx)
                continue

            active   = record.get("active_variant", 1)
            variants = record.get("variants", [])
            img_path = None
            for v in variants:
                if v.get("index") == active:
                    img_path = v.get("path")
                    break
            if img_path is None and variants:
                img_path = variants[0].get("path")

            if not img_path or not os.path.exists(img_path):
                bad_panels.append(panel_idx)

        if bad_panels:
            pages_with_missing_panels[page_num] = bad_panels

    # Also check special pages (narration/interstitial/act-break) for missing
    # panel images — but only report, never treat them as "missing pages" since
    # they already exist in the script as valid structured content.
    for page in script:
        if not (_is_special(page) or page.get("_act_break")):
            continue
        pnum = page.get("page")
        if pnum is None:
            continue
        pnum = int(pnum)
        page_key = f"page_{pnum:03d}"
        page_manifest = manifest.get(page_key, {})
        record = page_manifest.get("panel_01", {})
        if record:
            variants = record.get("variants", [])
            active   = record.get("active_variant", 1)
            img_path = None
            for v in variants:
                if v.get("index") == active:
                    img_path = v.get("path")
                    break
            if img_path is None and variants:
                img_path = variants[0].get("path")
            if not img_path or not os.path.exists(img_path):
                # Panel image missing for a special page — note it but don't
                # count it toward missing_pages (don't regenerate the whole page)
                logger.warning(
                    f"  [Validate] Special page {pnum} ({page.get('title','')}) "
                    f"has no panel image at {img_path!r}; editor can regenerate."
                )

    total = len(missing_pages) + sum(len(v) for v in pages_with_missing_panels.values())

    lines = [f"Validation found {total} issue(s):"]
    if not total:
        lines = ["Validation passed: all pages and panels are present ✓"]
    else:
        if missing_pages:
            lines.append(f"  • {len(missing_pages)} missing page(s): {missing_pages}")
        if pages_with_missing_panels:
            for pg, panels in sorted(pages_with_missing_panels.items()):
                lines.append(f"  • Page {pg}: missing panel image(s) {panels}")

    return {
        "script_page_numbers":       present,
        "expected_range":            range(min_page, max_page + 1),
        "missing_pages":             missing_pages,
        "pages_with_missing_panels": pages_with_missing_panels,
        "total_issues":              total,
        "summary":                   "\n".join(lines),
    }


